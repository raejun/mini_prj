package jyl;

public class JY_DTO {
	private String f_name;
	private String f_instrumental;
	private String f_genre;
	private String f_song;
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public String getF_instrumental() {
		return f_instrumental;
	}
	public void setF_instrumental(String f_instrumental) {
		this.f_instrumental = f_instrumental;
	}
	public String getF_genre() {
		return f_genre;
	}
	public void setF_genre(String f_genre) {
		this.f_genre = f_genre;
	}
	public String getF_song() {
		return f_song;
	}
	public void setF_song(String f_song) {
		this.f_song = f_song;
	}
	
	

}
